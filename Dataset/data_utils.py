def jitter_point_cloud(batch_data, sigma=0.01, clip=0.05):
    """ Randomly jitter points. jittering is per point.
        Input:
          BxNx3 array, original batch of point clouds
        Return:
          BxNx3 array, jittered batch of point clouds
          随机位移扰动点云中的每个点
    """
    B, N, C = batch_data.shape
    assert(clip > 0)
    jittered_data = np.clip(sigma * np.random.randn(B, N, C), -1*clip, clip)
    jittered_data += batch_data
    return jittered_data

def shift_point_cloud(batch_data, shift_range=0.1):
    """ Randomly shift point cloud. Shift is per point cloud.
        Input:
          BxNx3 array, original batch of point clouds
        Return:
          BxNx3 array, shifted batch of point clouds
          随机整体平移点云
    """
    B, N, C = batch_data.shape
    shifts = np.random.uniform(-shift_range, shift_range, (B,3))
    for batch_index in range(B):
        batch_data[batch_index,:,:] += shifts[batch_index,:]
    return batch_data


def random_scale_point_cloud(batch_data, scale_low=0.8, scale_high=1.25):
    """ Randomly scale the point cloud. Scale is per point cloud.
        Input:
            BxNx3 array, original batch of point clouds
        Return:
            BxNx3 array, scaled batch of point clouds
            随机整体缩放点云
    """
    B, N, C = batch_data.shape
    scales = np.random.uniform(scale_low, scale_high, B)
    for batch_index in range(B):
        batch_data[batch_index,:,:] *= scales[batch_index]
    return batch_data

def random_point_dropout(batch_pc, max_dropout_ratio=0.875):
    ''' batch_pc: BxNx3  随机舍弃点'''
    for b in range(batch_pc.shape[0]):
        dropout_ratio =  np.random.random()*max_dropout_ratio # 0~0.875
        drop_idx = np.where(np.random.random((batch_pc.shape[1]))<=dropout_ratio)[0]
        if len(drop_idx)>0:
            batch_pc[b,drop_idx,:] = batch_pc[b,0,:] # set to the first point
    return batch_pc

def point_cloud_voxelize(points, resolution, channel):
    #切成resolution*resolution*c
    #output:B*C*R*R

	batch_size, num_points, feature_dim = points.shape
	voxel = torch.zeros((batch_size, channel, resolution, resolution))
	max_coord = torch.max(points, dim=1)[0]
	min_coord = torch.min(points, dim=1)[0]
	interval = max_coord - min_coord
	eps = 0.000001
	step_x = interval[:, 0] / resolution
	step_y = interval[:, 1] / resolution
	step_z = interval[:, 2] / channel
	
	for i in range(batch_size):
		for j in range(num_points):
		
			idx_x = (points[i, j, 0] - min_coord[i, 0] - eps)/step_x[i]
			idx_y = (points[i, j, 1] - min_coord[i, 1] - eps)/step_y[i]
			idx_z = (points[i, j, 2] - min_coord[i, 2] - eps)/step_z[i]
			voxel[i, int(torch.floor(idx_z)), int(torch.floor(idx_x)), int(torch.floor(idx_y))] += torch.frac(idx_z)
			
	return voxel

def point_cloud_knn(batch_data,k):
    """
        input:BxNxC
        output:BxNxK(idx)
    """
    inner=-2*torch.matmul(batch_data,batch_data.transpose(2,1))#B*N*N,相当于所有点两两点乘
    xx=torch.sum(batch_data**2,dim=2,keepdim=True)#在维度2上求和并保留维度 最后变成B*N*1
    pairwise_distance=-xx-inner-xx.transpose(2,1)#变成B*N*N 距离取负
    idx=pairwise_distance.topk(k=k,dim=-1)[1]# 返回（value,index）(batch_size, num_points, k) 取k个最大数（距离取负相当于取k个最小距离）

    return idx


